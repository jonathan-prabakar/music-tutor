-- 0. Helper function for updated_at triggers
create or replace function handle_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

-- ============================================================
-- 1. profiles
-- ============================================================
create table profiles (
  id          uuid primary key references auth.users(id) on delete cascade,
  email       text not null,
  name        text,
  avatar_url  text,
  role        text not null check (role in ('student', 'tutor')) default 'student',
  created_at  timestamptz not null default now(),
  updated_at  timestamptz not null default now()
);

create index idx_profiles_role on profiles(role);

create trigger trg_profiles_updated_at
  before update on profiles
  for each row execute function handle_updated_at();

alter table profiles enable row level security;

create policy "authenticated users can read profiles"
  on profiles for select
  to authenticated
  using (true);

create policy "users can insert their own profile"
  on profiles for insert
  to authenticated
  with check (id = auth.uid());

create policy "users can update their own profile"
  on profiles for update
  to authenticated
  using (id = auth.uid())
  with check (id = auth.uid());

-- ============================================================
-- 2. student_profiles
-- ============================================================
create table student_profiles (
  id            uuid primary key references auth.users(id) on delete cascade,
  instruments   text[] not null default '{}',
  experience    text not null default 'beginner' check (experience in ('beginner', 'some', 'intermediate', 'advanced')),
  goal          text not null default 'fun' check (goal in ('fun', 'basics', 'perform', 'competitive')),
  created_at    timestamptz not null default now(),
  updated_at    timestamptz not null default now()
);

create index idx_student_profiles_experience on student_profiles(experience);
create index idx_student_profiles_goal on student_profiles(goal);

create trigger trg_student_profiles_updated_at
