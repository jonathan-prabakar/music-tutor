-- ============================================================
-- 4. lesson_requests
-- ============================================================
create table lesson_requests (
  id                  bigint generated by default as identity primary key,
  student_id          uuid not null references auth.users(id) on delete cascade,
  tutor_id            uuid not null references auth.users(id) on delete cascade,
  student_instruments text[] not null default '{}',
  student_experience  text not null,
  student_goal        text not null,
  status              text not null default 'pending' check (status in ('pending', 'accepted', 'declined')),
  created_at          timestamptz not null default now(),
  updated_at          timestamptz not null default now()
);

create index idx_lesson_requests_student_id on lesson_requests(student_id);
create index idx_lesson_requests_tutor_id on lesson_requests(tutor_id);
create index idx_lesson_requests_status on lesson_requests(status);

create trigger trg_lesson_requests_updated_at
  before update on lesson_requests
  for each row execute function handle_updated_at();

alter table lesson_requests enable row level security;

create policy "authenticated users can read lesson_requests"
  on lesson_requests for select
  to authenticated
  using (true);

create policy "students can create lesson_requests"
  on lesson_requests for insert
  to authenticated
  with check (student_id = auth.uid());

create policy "tutors can update lesson_request status"
  on lesson_requests for update
  to authenticated
  using (tutor_id = auth.uid())
  with check (tutor_id = auth.uid() and status in ('accepted', 'declined'));